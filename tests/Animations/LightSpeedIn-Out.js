/**LightSpeedIn-Out animations**/

/**LightSpeedIn animation**/
const lightSpeedIn = () => {
  let div = document.createElement("div");
  div.className = "lightSpeedIn";
  document.body.appenchild("div");
};

/**LightSpeedOut animation**/
const lightSpeedOut = () => {
  let div = document.createElement("div");
  div.className = "lightSpeedOut";
  document.body.appenchild("div");
};
